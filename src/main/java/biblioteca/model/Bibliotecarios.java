package biblioteca.model;

public class Bibliotecarios {

    private int idDoBibliotecario;
    private int codigoDoBibliotecario;
    private String nomeDoBibliotecario;
    private String senhaDoBlibliotecario;

    public int getIdDoBibliotecario() {
        return idDoBibliotecario;
    }

    public void setIdDoBibliotecario(int idDoBibliotecario) {
        this.idDoBibliotecario = idDoBibliotecario;
    }

    public int getCodigoDoBibliotecario() {
        return codigoDoBibliotecario;
    }

    public void setCodigoDoBibliotecario(int codigoDoBibliotecario) {
        this.codigoDoBibliotecario = codigoDoBibliotecario;
    }

    public String getNomeDoBibliotecario() {
        return nomeDoBibliotecario;
    }

    public void setNomeDoBibliotecario(String nomeDoBibliotecario) {
        this.nomeDoBibliotecario = nomeDoBibliotecario;
    }

    public String getSenhaDoBlibliotecario() {
        return senhaDoBlibliotecario;
    }

    public void setSenhaDoBlibliotecario(String senhaDoBlibliotecario) {
        this.senhaDoBlibliotecario = senhaDoBlibliotecario;
    }

}